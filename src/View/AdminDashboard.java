    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        

    String title = txtTitle.getText().trim();
    String artist = txtArtist.getText().trim();
    String genre = txtGenre.getText().trim();

    if (title.isEmpty() || artist.isEmpty() || genre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all fields");
        return;
    }

    if (selectedIndex == -1) {
        SongController.addSong(title, artist, genre);
        JOptionPane.showMessageDialog(this, "Song added successfully");
    } else {
        SongController.updateSong(selectedIndex, title, artist, genre);
        JOptionPane.showMessageDialog(this, "Song updated successfully");
        selectedIndex = -1;
    }

    loadSongsToTable();

    CardLayout cl = (CardLayout) mainPanel.getLayout();
    cl.show(mainPanel, "songs");

    }//GEN-LAST:event_jButton10ActionPerformed

