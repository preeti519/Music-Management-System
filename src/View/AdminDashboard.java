    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        

    int row = jTable1.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Select a song to edit");
        return;
    }

    // store selected index
    selectedIndex = row;

    // load data into text fields
    txtTitle.setText(jTable1.getValueAt(row, 0).toString());
    txtArtist.setText(jTable1.getValueAt(row, 1).toString());
    txtGenre.setText(jTable1.getValueAt(row, 2).toString());

    // switch to Add/Edit panel
    CardLayout cl = (CardLayout) mainPanel.getLayout();
    cl.show(mainPanel, "addSong");
    

    }//GEN-LAST:event_jButton8ActionPerformed

