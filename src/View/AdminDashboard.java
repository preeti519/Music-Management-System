    private void loadSongsToTable() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // clear table

    for (Song s : SongController.getAllSongs()) {
        model.addRow(new Object[]{
            s.getTitle(),
            s.getArtist(),
            s.getGenre()
        });
    }
}

